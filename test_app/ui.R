library(shiny)
library(DT)

ui <- fluidPage(
  titlePanel("Exploratory Data Analysis"),
  
  sidebarLayout(
    sidebarPanel(
      fileInput("file1", "Choose CSV File",
                accept = c(
                  "text/csv",
                  "text/comma-separated-values,text/plain",
                  ".csv")
      )
    ),
    
    mainPanel(
      DTOutput("contents")  # DataTable output
    )
  )
)
